// ---------------------------------------------------------------------------
// Build the FeatureCollection
// ---------------------------------------------------------------------------

function buildFeature(
  meta: StateMeta,
): GeoJSON.Feature<GeoJSON.Polygon, VenezuelaStateFeatureProperties> {
  const coords = STATE_POLYGONS[meta.name]
  if (!coords) {
    // Fallback: create a small polygon around the center
    const [lng, lat] = meta.center
    const d = 0.15
    return {
      type: 'Feature',
      properties: {
        id: meta.code,
        name: meta.name,
        code: meta.code,
        center: meta.center,
        bounds: meta.bounds,
      },
      geometry: {
        type: 'Polygon',
        coordinates: [
          [
            [lng - d, lat - d],
            [lng + d, lat - d],
            [lng + d, lat + d],
            [lng - d, lat + d],
            [lng - d, lat - d],
          ],
        ],
      },
    }
  }

  return {
    type: 'Feature',
    properties: {
      id: meta.code,
      name: meta.name,
      code: meta.code,
      center: meta.center,
      bounds: meta.bounds,
    },
    geometry: {
      type: 'Polygon',
      coordinates: coords,
    },
  }
}

export const VENEZUELA_GEOJSON: VenezuelaGeoJSON = {
  type: 'FeatureCollection',
  features: STATES.map(buildFeature),
}

