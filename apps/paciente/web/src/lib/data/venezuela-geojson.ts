// GeoJSON FeatureCollection for Venezuela's 24 political divisions (23 states + Distrito Capital)
// Coordinates are simplified polygons in [longitude, latitude] format (WGS84)
// Sources: GADM, Natural Earth, and manual verification against official maps
// Centroids and bounds are approximate geographic values for flyTo / fitBounds

export interface VenezuelaStateFeatureProperties {
  id: string
  name: string
  code: string // ISO 3166-2:VE
  center: [number, number] // [lng, lat]
  bounds: [[number, number], [number, number]] // [[west, south], [east, north]]
}

export type VenezuelaGeoJSON =
  GeoJSON.FeatureCollection<GeoJSON.Polygon | GeoJSON.MultiPolygon, VenezuelaStateFeatureProperties>

