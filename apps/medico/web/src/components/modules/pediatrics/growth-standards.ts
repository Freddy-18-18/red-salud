/**
 * Get growth standard data for a specific chart type and sex.
 */
export function getGrowthStandard(
  chartType: ChartType,
  sex: Sex,
): GrowthStandardData | null {
  switch (chartType) {
    case 'weight-for-age':
      return sex === 'male' ? WEIGHT_FOR_AGE_BOYS : WEIGHT_FOR_AGE_GIRLS;
    case 'height-for-age':
      return sex === 'male' ? HEIGHT_FOR_AGE_BOYS : HEIGHT_FOR_AGE_GIRLS;
    case 'head-circumference-for-age':
      return sex === 'male' ? HEAD_CIRC_BOYS : HEAD_CIRC_GIRLS;
    case 'bmi-for-age':
      return sex === 'male' ? BMI_FOR_AGE_BOYS : BMI_FOR_AGE_GIRLS;
    case 'weight-for-height':
      // Weight-for-height uses height as X axis; we approximate via age mapping
      return null;
    default:
      return null;
  }
}

/**
 * Interpolate a percentile value at a given age in months.
 * Uses linear interpolation between the two nearest data points.
 */
export function interpolatePercentile(
  data: GrowthStandardData,
  percentile: PercentileKey,
  ageMonths: number,
): number | null {
  const { months, percentiles } = data;
  const values = percentiles[percentile];

  if (ageMonths < months[0] || ageMonths > months[months.length - 1]) {
    return null;
  }

  // Find surrounding indices
  let lo = 0;
  let hi = months.length - 1;

  for (let i = 0; i < months.length - 1; i++) {
    if (months[i] <= ageMonths && months[i + 1] >= ageMonths) {
      lo = i;
      hi = i + 1;
      break;
    }
  }

  if (months[lo] === ageMonths) return values[lo];
  if (months[hi] === ageMonths) return values[hi];

  // Linear interpolation
  const t = (ageMonths - months[lo]) / (months[hi] - months[lo]);
  return values[lo] + t * (values[hi] - values[lo]);
}

/**
 * Estimate which percentile a measurement falls on.
 * Returns a number between 0 and 100 (approximate).
 */
export function estimatePercentile(
  data: GrowthStandardData,
  ageMonths: number,
  value: number,
): number | null {
  const percentileKeys: PercentileKey[] = ['p3', 'p15', 'p50', 'p85', 'p97'];
  const percentileValues = [3, 15, 50, 85, 97];

  const interpolated = percentileKeys.map((key) =>
    interpolatePercentile(data, key, ageMonths),
  );

  // All null means out of range
  if (interpolated.every((v) => v === null)) return null;

  // Below P3
  if (interpolated[0] !== null && value <= interpolated[0]) return 1;
  // Above P97
  if (interpolated[4] !== null && value >= interpolated[4]) return 99;

  // Find surrounding percentiles and interpolate
  for (let i = 0; i < interpolated.length - 1; i++) {
    const lo = interpolated[i];
    const hi = interpolated[i + 1];
    if (lo !== null && hi !== null && value >= lo && value <= hi) {
      const t = (value - lo) / (hi - lo);
      return Math.round(
        percentileValues[i] + t * (percentileValues[i + 1] - percentileValues[i]),
      );
    }
  }

  return 50;
}

/**
 * Detect if a measurement has crossed percentile lines compared to a previous one.
 * Returns the shift in percentile bands (e.g., -2 means dropped 2 bands).
 */
export function detectPercentileCrossing(
  data: GrowthStandardData,
  prevAgeMonths: number,
  prevValue: number,
  currAgeMonths: number,
  currValue: number,
): { shift: number; severity: 'none' | 'mild' | 'moderate' | 'severe' } {
  const prevP = estimatePercentile(data, prevAgeMonths, prevValue);
  const currP = estimatePercentile(data, currAgeMonths, currValue);

  if (prevP === null || currP === null) {
    return { shift: 0, severity: 'none' };
  }

  // Determine which band each falls in
  function getBand(p: number): number {
    if (p < 3) return 0;
    if (p < 15) return 1;
    if (p < 50) return 2;
    if (p < 85) return 3;
    if (p < 97) return 4;
    return 5;
  }

  const prevBand = getBand(prevP);
  const currBand = getBand(currP);
  const shift = currBand - prevBand;

  let severity: 'none' | 'mild' | 'moderate' | 'severe' = 'none';
  const absShift = Math.abs(shift);
  if (absShift >= 3) severity = 'severe';
  else if (absShift >= 2) severity = 'moderate';
  else if (absShift >= 1) severity = 'mild';

  return { shift, severity };
}
